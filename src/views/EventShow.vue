<template>
  <h1>Event showing #{{ id }}</h1>

</template>

<script>
import EventService from "@/services/EventService.js";

export default {
  props: ["id"],
  data() {
    return {
        event: {}
    }
  },
  created() {
    EventService.getEvent(this.id)
      .then(response => {
        this.event = response.data
      })
      .catch(error => {
        console.log("Error: " + error.response)
      })
  }
};
</script>
